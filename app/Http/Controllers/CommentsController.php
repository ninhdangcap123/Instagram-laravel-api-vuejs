    public function ToggleLike(Comment $comment)
    {
        $comment->toggleLike();
        $comment->save();
        $comment->score = $comment->likesCount();
        $comment->liked = $comment->isLiked();
        return response()->json([
                            'comment' => $comment
                        ]);
    }


    public function isLiked(Comment $comment)
    {
        $liked = $comment->isLiked();
        return response()->json([
                            'liked' => $liked
                        ]);
